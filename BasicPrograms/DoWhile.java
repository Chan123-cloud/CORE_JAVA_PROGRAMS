package BasicPrograms;

public class DoWhile {

	public static void main(String[] args) {
		
		int a = 35;
		
		do {
			
			if(a%2!=0) {
				
				System.out.println(a + " ODD NUMBER");
			}
			
		a++;	
		}
		
	while(a>=89); // condition is at last so if it also false statement it will execute once without checking the condition.

		
		

}
}
