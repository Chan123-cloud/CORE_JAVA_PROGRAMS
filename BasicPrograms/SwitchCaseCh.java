package BasicPrograms;

public class SwitchCaseCh {
	
	
	public static void main(String[] args) {
		
				
				char A = 65;    // Passing the ASCCI  value of A
				
				switch (A) {
				
				case 'A': {       //Here the A will accepts it and execute.
					
					System.out.println("IT IS VALUE OF A");
					
					break;
					
					
				}
				
				case 'B':{  
					
					System.out.println("IT IS VALUE OF B");
					
					break;
				                                        
				}     
				
                case 'C':{  
					
					System.out.println("IT IS VALUE OF C");
					
					break;
				                                        
				}     
				
			
				default :{
					
					System.out.println("IT IS SOME OTHER CHARACTER VALUE");
				}
					
				}
			}

	}


